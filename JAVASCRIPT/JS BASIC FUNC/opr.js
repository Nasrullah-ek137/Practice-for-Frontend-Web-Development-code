// ARITHMETIC OPR (+,-,*,**,/,%,++,--)
var a=10;
var b=20;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a**b);
console.log(a/b);
console.log(a%b);
console.log(a++);
console.log(b--);

// ASSIGNMENT OPR  (=,+=,-=,*=,/=,%=,**=)
var x=10;
console.log(x);
x=x+3;
console.log(x);
x=x-5;
console.log(x);
x=x*2;
console.log(x);
x=x/2;
console.log(x);
x=x%3;
console.log(x);
x=x**3;
console.log(x);

// COMPARISON OPR (<,>,<=,>=,==,!=,===)
var s=200;
var t=100;
console.log(s<t);
console.log(s>t);
console.log(s<=t);
console.log(s>=t);
console.log(s==t);
console.log(s!=t);
var n="200";
console.log(s===n);

// LOGICAL OPR (&&,||,!)
var g=1023;
var f=1000;
console.log(f==1000 && g>=f);
console.log(g==10087 || f<=g);
console.log(f!=g);








